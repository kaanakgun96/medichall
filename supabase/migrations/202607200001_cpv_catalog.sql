-- ---------------------------------------------------------------------------
-- Seçici UI beslemesi: gruplar + her düğümün BESLEMEDEKİ CANLI ihale sayısı.
-- Sayılar cpv_codes_norm'dan prefix eşleşmesiyle gelir (aile mantığı) —
-- yalnız gerçek sayı gösterilir, veri yoksa 0 döner (uydurma yok).
-- ---------------------------------------------------------------------------
create or replace function public.cpv_catalog_with_counts(p_max_depth int default 5)
returns table (
  code text, code_full text, label_en text, depth smallint,
  parent_code text, open_tender_count bigint
)
language sql stable security definer set search_path = public
as $$
  select c.code, c.code_full, c.label_en, c.depth, c.parent_code,
         (select count(*) from public.tenders t
           where t.status = 'open'
             and exists (select 1 from unnest(t.cpv_codes_norm) n
                          where n like rtrim(c.code, '0') || '%'))
  from public.cpv_catalog c
  where c.depth <= greatest(2, least(coalesce(p_max_depth, 5), 8))
  order by c.code;
$$;

grant execute on function public.cpv_catalog_with_counts(int) to anon, authenticated;
